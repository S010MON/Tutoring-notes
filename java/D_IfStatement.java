public class D_IfStatement
{
    public static void main(String[] args)
    {
        if( 1 == 1)
        {
            // This code runs if the inside of the brackets evaluates to true
            System.out.println("This is a TRUE statement");
        }
        else
        {
            // This code will run if the inside of the brackets evaluates to false
            System.out.println("This is a FALSE statement");
        }

    }

}
